  "将", "已", "可", "把", "让", "向", "又", "更", "比", "非",
  "并", "其", "这个", "那个", "什么", "如何", "怎么", "为什么",
  // 连接词
  "而且", "但是", "因为", "所以", "如果", "虽然", "不过", "然而",
  "同时", "此外", "另外", "然后", "接着", "于是", "因此", "总之",
  // 量词/代词
  "个", "些", "种", "次", "年", "月", "日", "时", "分",
  "每", "各", "某", "该", "本", "此", "其他", "其它",
